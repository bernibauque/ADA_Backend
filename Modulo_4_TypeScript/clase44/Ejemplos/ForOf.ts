// Ejemplo básico con un array
export let lenguajes = ["JavaScript", "Python", "TypeScript"];

for (let lenguaje of lenguajes) {
    console.log(`Ejemplo Array: ${lenguaje}`);
}

// En este caso, el bucle for...of recorre cada valor del array lenguajes. 
// En cada iteración, la variable lenguaje toma el valor de uno de los 
// elementos del array (en este caso, "JavaScript", "Python", y "TypeScript").

console.log('-----------------------------------------------');

// Ejemplo con una cadena de texto
let saludo = "Hola";

for (let letra of saludo) {
    console.log(`Ejemplo Cadena de texto: ${letra}`);
}

// En este ejemplo, el bucle recorre cada letra de la cadena "Hola", 
// y en cada iteración imprime una letra.





