// Exporta la clase Calculator como el valor por defecto del módulo.
export default class Calculator {

    // Método que suma dos números y retorna el resultado.
    add(a: number, b: number): number {
        return a + b;  // Retorna la suma de a y b.
    }

    // Método que resta dos números y retorna el resultado.
    subtract(a: number, b: number): number {
        return a - b;  // Retorna la diferencia entre a y b.
    }
}





